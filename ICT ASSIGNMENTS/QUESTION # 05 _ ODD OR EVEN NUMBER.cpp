//Answer: An if-else statement is suitable for this task because it allows us to check if a number is even or odd based on a single condition. If the condition is true (i.e., the number is even), the code inside the if block is executed. If the condition is false (i.e., the number is odd), the code inside the else block is executed. This makes the code more concise and easier to read than using multiple if statements.
